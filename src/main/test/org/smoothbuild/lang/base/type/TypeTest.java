        r.add(arguments(a(a(A)), NOTHING, bm(A, LOWER, NOTHING)));
        r.add(arguments(a(a(A)), a(NOTHING), bm(A, LOWER, NOTHING)));
        r.add(arguments(a(a(A)), a(a(NOTHING)), bm(A, LOWER, NOTHING)));

