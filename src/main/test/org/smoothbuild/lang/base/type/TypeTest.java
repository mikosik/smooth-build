        r.add(arguments(a(f(A)), NOTHING, bm(A, LOWER, NOTHING)));
        r.add(arguments(a(f(STRING, A)), NOTHING, bm(A, UPPER, ANY)));

        r.add(arguments(f(a(A)), NOTHING, bm(A, LOWER, NOTHING)));
        r.add(arguments(f(BOOL, a(A)), NOTHING, bm(A, UPPER, ANY)));
